def fibonacci():
    # Фибоначчи тізбегі үшін бастапқы мәндерді инициализациялау
    a, b = 0, 1
    while True:
        # Ағымдағы реттілік мәнін қайтару
        yield a
        # Келесі Фибоначчи санының мәндерін жаңарту
        a, b = b, a + b


#   Фибоначчи тізбегінің генераторын құру
fib_generator = fibonacci()

# Фибоначчидің алғашқы n сандарын шығару
for _ in range(20):
    print(next(fib_generator))  # Получение следующего значения из генератора


